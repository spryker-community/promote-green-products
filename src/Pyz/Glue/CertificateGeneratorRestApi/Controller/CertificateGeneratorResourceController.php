namespace Pyz\Glue\CertificateGeneratorRestApi\Controller;

use Spryker\Glue\GlueApplication\Rest\Request\Data\RestRequestInterface;
use Spryker\Glue\Kernel\Controller\AbstractController;

/**
 * @method \Pyz\Glue\CertificateGeneratorRestApi\CertificateGeneratorRestApiFactory getFactory()
 */
class CertificateGeneratorResourceController extends AbstractController
{
    /**
     * @param \Spryker\Glue\GlueApplication\Rest\Request\Data\RestRequestInterface $restRequest
     *
     * @return \Spryker\Glue\GlueApplication\Rest\JsonApi\RestResponseInterface
     */
    public function getAction(RestRequestInterface $restRequest)
    {
        return $this->getFactory()
        ->createCertificateGeneratorReader()
        ->process($restRequest);
    }
}
